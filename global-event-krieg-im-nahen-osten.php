                        <h1>Krieg im Nahen Osten</h1>
                        <p>
                            Der aktuelle Krieg im Nahen Osten eskalierte am 7. Oktober 2023, als militante Kämpfer der Hamas überraschend Israel angriffen. Dies führte zu einem umfassenden Gegenschlag Israels im Gazastreifen. Die Region ist jedoch seit Jahrzehnten von Gewalt, Spannungen und Kriegen geprägt – mit historischen Wurzeln bis in die Mitte des 20. Jahrhunderts. Frühere Eskalationen, etwa 2008, 2014 oder 2021, hatten bereits zu schwerwiegenden humanitären Krisen geführt, jedoch ohne langfristige Lösung des Konflikts.
                            <br><br>
                            Der Angriff von 2023 markiert jedoch einen Wendepunkt. Innerhalb weniger Stunden rief Israel den Ausnahmezustand aus und leitete eine groß angelegte Militäroperation gegen die Hamas im Gazastreifen ein. In der Folge wurden tausende Luftangriffe auf Infrastrukturen, Kommandozentralen und Wohngebiete durchgeführt – begleitet von einer Blockade von Wasser, Strom und Hilfsgütern. Gleichzeitig wurde eine Bodenoffensive vorbereitet.
                            <br><br>
                            Die internationale Gemeinschaft reagierte gespalten: Während westliche Staaten Israel das Recht auf Selbstverteidigung zusprachen, wuchs die Kritik an der humanitären Situation in Gaza rasant. Viele arabische Staaten, internationale Organisationen und NGOs warnten vor einer humanitären Katastrophe und riefen zu einem Waffenstillstand auf.
                            <br><br>
                            Die wirtschaftlichen Folgen dieses Konflikts sind global spürbar: Der Nahe Osten ist eine der wichtigsten Regionen für die weltweite Energieversorgung. Spannungen rund um die Straße von Hormus oder Angriffe auf Infrastruktur im Libanon, Syrien oder dem Westjordanland wirken sich direkt auf Ölpreise und Lieferketten aus. Auch der Suezkanal, eine der wichtigsten Handelsrouten der Welt, ist durch politische Unsicherheit und mögliche Blockaden gefährdet.
                            <br><br>
                            Der anhaltende Konflikt erschwert zusätzlich den Zugang zu humanitärer Hilfe, medizinischer Versorgung und Lebensmitteln – insbesondere im dicht besiedelten Gazastreifen. Gleichzeitig hat die Eskalation zu einer massiven Fluchtbewegung innerhalb und ausserhalb der Region geführt. Tausende Menschen wurden verletzt oder getötet, Hunderttausende vertrieben.
