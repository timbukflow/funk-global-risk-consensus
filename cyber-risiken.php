                <div class="rdsolution rbm bg">
                    <h1>Zu den wichtigsten Cyber-Security-Massnahmen gehören:</h1>
                    <ul>
                        <li>Ein vollständiges Asset-Management (Inventar der IT-Systeme, Anwendungen und Daten)</li>
                        <li>Kontinuierliche Schutzbedarfsanalyse der Assets sowie die Ableitung entsprechender Massnahmen</li>
                        <li>Jährliche Sensibilisierungstrainings von Mitarbeitenden hinsichtlich Cyber-Risiken (vor allem Phishing) und Datenschutz</li>
                        <li>Starke E-Mail-Filter, die schadhafte E-Mails anhand Absender, Inhalt und Anhänge blockieren</li>
                        <li>Starke Passwörter sowie Authentifizierung durch mehrere Faktoren (MFA) für Mitarbeitende, Administratoren und Dienstleister</li>
                        <li>Vergabe von Zugriffsberechtigungen nach dem Least-Privilege- und/oder Need-to-know-Prinzip</li>
                        <li>Verschlüsselung schützenswerter oder besonders schützenswerter Personendaten (in transit, at rest und auf Backups)</li>
                        <li>Backup der geschäftskritischen Daten nach der 3-2-1-Regel wobei die externe Kopie mittels eines Cloud-Backups oder eines offline Backups sichergestellt wird</li>
                        <li>Sicherstellung eines reaktionsschnellen Vulnerability- und Patchmanagements, welche technische Schwachstellen unmittelbar identifizieren und diese nach Veröffentlichung und zeitnahe Installation des Sicherheitspatches schliessen</li>
                        <li>Sinnvolle und strikte Segmentierung des Netzwerkes in mehrere Zonen (geografisch, organisatorisch, Altsysteme)</li>
                        <li>Ausrollen einer Endpoint Detection & Response-Lösung auf alle Endgeräte sowie eines strukturierten Prozesses zur Reaktion auf die daraus resultierende Alerts</li>
                        <li>Regelmässige Penetration-Tests</li>
                        <li>Vorbereitete und geübte Incident Response, Disaster Recovery, Business Continuity und Krisenpläne für den Ernstfall</li>
                        <li>Überprüfung der Sinnhaftigkeit und Abschluss einer risikogerechten Cyber-Versicherung</li>
                        <li>Sicherstellung der Konformität mit den geltenden Datenschutzgesetzen und der daraus resultierenden technischen und organisatorischen Massnahmen (TOM)</li>
                    </ul>
