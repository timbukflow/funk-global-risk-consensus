                    <h1>Neue Technologien: Chancen und Risiken im digitalen Wandel</h1>

                    <h2>Rasante Entwicklung</h2>
                    <p>
                        Die Entwicklung der digitalen Technologien beschleunigt sich Jahr für Jahr. IoT und KI übernehmen je länger je mehr Arbeiten und unterstützen viele Prozesse in allen Bereichen des Alltags. Dank neuer Kommunikations- und Kollaborationstools ist Remote Work mittlerweile weit verbreitet und erleichtert, sodass es nicht mehr zwingend erforderlich ist, die gesamte Arbeitszeit vor Ort zu verbringen. Alle diese neuen Tools und Technologien bringen, neben ihren Vorteilen, auch viele Risiken mit sich. Eine dieser Risiken besteht darin, den Wandel zu verpassen und dadurch einen Nachteil gegenüber der Konkurrenz zu haben, die produktiver und effizienter wird.
                    </p>

                    <h2>Überhastete Implementierung und Konzeptlosigkeit</h2>
                    <p>
                        Eine saubere Implementierung neuer Technologien kann zu einem enormen Produktivitätsgewinn führen. Gleichzeitig besteht jedoch ein erhebliches Risiko, dass die Prozesse nicht mehr wie gewohnt funktionieren, wenn die neuen Technologien nicht nahtlos in die vertraute Arbeitsumgebung integriert werden. Schlecht geschützte IoT-Geräte stellen oft Einfallstore für Hacker dar. Nicht funktionierende oder nicht wie gewünscht funktionierende Tools verlangsamen Prozesse und führen nicht nur zu Effizienzverlusten, sondern erfordern häufig auch unerwartete Investitionen zur Problemlösung. Eine Ursache für das Auftreten solcher Risiken liegt in der grossen kulturellen Kluft zwischen Entscheidungsträgern und den IT-Abteilungen. Daher ist es wichtig, dass das Verständnis für neue Technologien bis in die obersten Ebenen der Unternehmensstruktur durchdringt.
                    </p>

                    <div class="hidden-text">
                        <h2>Covid-19 als Treiber des Wandels</h2>
                        <p>
                        Die Eintretenswahrscheinlichkeit dieses Risikos hat mit der Corona-Pandemie verringert. Dies liegt hauptsächlich daran, dass durch die Lockdown-Massnahmen der Wandel zum Home-Office und zur digitalen Kommunikation erzwungen wurde. Darüber hinaus hat sich das Home-Office als gängige Praxis etabliert und wird auch nach der Pandemie weiterhin genutzt. Das Risiko im Zusammenhang mit neuen Technologien und dem Verpassen des Wandels ist daher nicht mehr so gross wie zuvor, jedoch haben sich die Cyber-Risiken um weitere Nuancen erweitert.
                        </p>
