                        <h1>Datenschutzgrundverordnung (DSGVO)</h1>
                        <p>
                            Die Datenschutzgrundverordnung (DSGVO) ist eine EU-Verordnung, die am 25. Mai 2018 in Kraft getreten ist. Sie legt einheitliche Datenschutzregeln für alle EU-Mitgliedstaaten fest und stärkt die Rechte von Personen in Bezug auf den Schutz ihrer personenbezogenen Daten.
                            <br><br>
                            Die DSGVO hat das Ziel, den Schutz personenbezogener Daten zu verbessern und den freien Verkehr dieser Daten innerhalb der EU zu gewährleisten. Sie gilt für alle Organisationen, die personenbezogene Daten von EU-Bürgern verarbeiten, unabhängig davon, ob sich die Organisation innerhalb oder ausserhalb der EU befindet.
                            <br><br>
                            Die wichtigsten Prinzipien der DSGVO sind Transparenz, Zweckbindung, Datenminimierung, Richtigkeit, Speicherbegrenzung, Integrität und Vertraulichkeit. Die Verordnung verlangt von Organisationen, dass sie klare und verständliche Informationen bereitstellen, wie sie personenbezogene Daten verarbeiten und zu welchem Zweck. Personen haben das Recht auf Zugang, Berichtigung, Löschung und Übertragbarkeit ihrer Daten. Die DSGVO legt auch strenge Regeln für die Einholung von Einwilligungen zur Verarbeitung personenbezogener Daten fest.
                            <br><br>
                            Die Verordnung stellt hohe Anforderungen an die Sicherheit und den Schutz personenbezogener Daten. Organisationen müssen geeignete technische und organisatorische Massnahmen ergreifen, um die Daten vor unbefugtem Zugriff, Verlust oder Diebstahl zu schützen. Bei Verletzungen des Datenschutzes müssen die Organisationen innerhalb bestimmter Fristen Benachrichtigungen an die zuständigen Datenschutzbehörden und betroffenen Personen senden.
                            <br><br>
                            Die Datenschutzbehörden der EU-Mitgliedstaaten sind für die Durchsetzung der DSGVO zuständig. Diese können bei Verstössen gegen die Verordnung hohe Geldbussen verhängen. Die Höhe der Geldbussen hängt von der Art des Verstosses und der Schwere der Datenschutzverletzung ab.
                            <br><br>
                            Die Datenschutzgrundverordnung stellt eine bedeutende Weiterentwicklung im Bereich des Datenschutzes dar und hat weltweit Auswirkungen auf Organisationen, die personenbezogene Daten von EU-Bürgern verarbeiten. Dies führte innert kurzer Zeit dazu, dass alle Organisationen, welche personenbezogene Daten verarbeiteten, ihre Prozesse in der IT sowie in ihrer Compliance-Abteilungen umstellen mussten um die Anforderungen der DSGVO zu erfüllen. Für viele Firmen war dies bis heute nicht vollständig möglich und sind somit nicht DSGVO-Konform unterwegs. Dies ist auch an den vielen Bussen zu erkennen, die laufend ausgesprochen werden.
                            <br><br>
                            Organisationen, welche mit dem Handel von Daten Geld verdienten, mussten Ihre Wertschöpfungskette umstellen oder sogar komplett ändern.
